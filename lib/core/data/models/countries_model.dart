import 'dart:convert';

class CountriesModel {
  final String id;
  final String name;
  final String currencySymbol;
  final String currency;
  final String code;
  final String phoneCode;
  CountriesModel({
    required this.id,
    required this.name,
    required this.currencySymbol,
    required this.currency,
    required this.code,
    required this.phoneCode,
  });

  CountriesModel copyWith({
    String? id,
    String? name,
    String? currencySymbol,
    String? currency,
    String? code,
    String? phoneCode,
  }) {
    return CountriesModel(
      id: id ?? this.id,
      name: name ?? this.name,
      currencySymbol: currencySymbol ?? this.currencySymbol,
      currency: currency ?? this.currency,
      code: code ?? this.code,
      phoneCode: phoneCode ?? this.phoneCode,
    );
  }

  Map<String, dynamic> toMap() {
    return <String, dynamic>{
      'id': id,
      'name': name,
      'currencySymbol': currencySymbol,
      'currency': currency,
      'code': code,
      'phoneCode': phoneCode,
    };
  }

  factory CountriesModel.fromMap(Map<String, dynamic> map) {
    return CountriesModel(
      id: map['id'] as String,
      name: map['name'] as String,
      currencySymbol: map['currencySymbol'] as String,
      currency: map['currency'] as String,
      code: map['code'] as String,
      phoneCode: map['phoneCode'] as String,
    );
  }

  String toJson() => json.encode(toMap());

  factory CountriesModel.fromJson(String source) =>
      CountriesModel.fromMap(json.decode(source) as Map<String, dynamic>);

  @override
  String toString() {
    return 'CountriesModel(id: $id, name: $name, currencySymbol: $currencySymbol, currency: $currency, code: $code, phoneCode: $phoneCode)';
  }

  @override
  bool operator ==(covariant CountriesModel other) {
    if (identical(this, other)) return true;

    return other.id == id &&
        other.name == name &&
        other.currencySymbol == currencySymbol &&
        other.currency == currency &&
        other.code == code &&
        other.phoneCode == phoneCode;
  }

  @override
  int get hashCode {
    return id.hashCode ^
        name.hashCode ^
        currencySymbol.hashCode ^
        currency.hashCode ^
        code.hashCode ^
        phoneCode.hashCode;
  }

